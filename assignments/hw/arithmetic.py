def add(a, b):
    sum = a + b
    return sum

def subtract(a, b):
    diff = a - b
    return diff

def multiply(a, b):
    prod = a * b
    return prod

def divide(a, b):
    quotient = a / b
    return quotient

def power(a, b):
    exponent = a**b 
    return exponent

def mod(a, b):
    return a % b 